// operator augmented berguna untuk mempersingkat penulisan kode program

let hasil = 1+2; //3
document.writeln("<p> 1 + 2 = " + hasil + "</P>");
let hasilasli = hasil;

hasil -= 1; //2
document.writeln("<p>" + hasilasli + " - 1 = " + hasil + "</p>");
hasilasli = hasil;

hasil *= 2; //4
document.writeln("<p>" + hasilasli + " x 2 = " + hasil + "</p>");
hasilasli = hasil;

hasil /= 2; //2
document.writeln("<p>" + hasilasli + " : 2 = " + hasil + "</p>");
hasilasli = hasil;