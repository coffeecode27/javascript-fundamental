
/** operator Aritmatika 

+ (pertambahan)
- (pengurangan)
* (perkalian)
** (exponensial)
/ (pembagian)
% (sisa bagi atau modulo)

**/

let hasil = 1+2; //3
document.writeln("<p> 1 + 2 = " + hasil + "</P>");
let hasilasli = hasil;

hasil2 = hasil - 1; //2
document.writeln("<p>" + hasilasli + " - 1 = " + hasil2 + "</p>");
hasilasli = hasil2;

hasil3 = hasil2 * 2; //4
document.writeln("<p>" + hasilasli + " x 2 = " + hasil3 + "</p>");
hasilasli = hasil3;